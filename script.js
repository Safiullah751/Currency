// Function to search and display country details
function searchCountry() {
    // Get the search term from the input field
    const searchTerm = document.getElementById('search-input').value.toLowerCase();

    // Clear previous results
    const resultsContainer = document.getElementById('results');
    resultsContainer.innerHTML = '';

    // Check if search term is empty
    if (searchTerm === '') {
        resultsContainer.innerHTML = '<p>Please enter a country name.</p>';
        return;
    }

    // Filter the country array for matches
    const results = countryArray.filter(item => item.country.toLowerCase().includes(searchTerm));

    // Display results
    if (results.length > 0) {
        results.forEach(item => {
            const countryDiv = document.createElement('div');
            countryDiv.className = 'country-item';
            
            // Add image
            const countryImg = document.createElement('img');
            countryImg.src = item.country_img;
            countryImg.alt = item.country;

            // Add details
            const detailsDiv = document.createElement('div');
            detailsDiv.className = 'details';
            detailsDiv.innerHTML = `
                <strong>Country:</strong> ${item.country}<br>
                <strong>Currency:</strong> ${item.currency_name}
            `;

            // Append image and details to the country div
            countryDiv.appendChild(countryImg);
            countryDiv.appendChild(detailsDiv);

            // Append the country div to the results container
            resultsContainer.appendChild(countryDiv);
        });
    } else {
        resultsContainer.innerHTML = '<p class="error">No results found for the given country name.</p>';
    }
}
